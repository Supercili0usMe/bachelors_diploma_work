from typing import List


class Solution:
    def largestLocal(grid: List[List[int]]) -> List[List[int]]:
        ans = []
        n = len(grid)
        for i in range(1, n-1):
            temp = []
            for j in range(1, n-1):
                temp_1 = []
                for k in range(3):
                    temp_1.extend(grid[i-1+k][j-1:j+2])
                temp.append(max(temp_1))
            ans.append(temp)

        return ans

